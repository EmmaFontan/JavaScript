///OPERACIONES CON LOS TIPOS DE DATOS



///OPERACIONES CON DATOS DE TIPO NUMBER

let numerito1;
let numerito2;
let total;

numerito1 = 6;
numerito2 = 5;


///SUMA
total = numerito1 + numerito2;

///RESTA
total = numerito1 - numerito2;

///PRODUCTO /// multiplicacion
total = numerito1 * numerito2; /// 15

//DIVISION
total = numerito1 / numerito2; ///0.6



